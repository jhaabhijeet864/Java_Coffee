package Practical;

class Str {
    public static void main(String[] args) {
        String s = "Java";

        System.out.println("Length of string: " + s.length());
        System.out.println("Uppercase string: " + s.toUpperCase());
        System.out.println("Lowercase string: " + s.toLowerCase());
        System.out.println("Character at index 1: " + s.charAt(1));
        System.out.println("Concatenated string: " + s.concat(" Programming"));
    }
}
